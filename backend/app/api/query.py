class NumberedCanvas(canvas.Canvas):
    """Dynamically applies custom headers, running footers, and a persistent 
    non-legal-advice disclaimer across multiple pages without splitting artifacts."""
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_elements(num_pages)
            super().showPage()
        super().save()

    def draw_page_elements(self, page_count):
        self.saveState()
        
        # Upper Running Header Definition
        self.setFont("Helvetica-Bold", 8)
        self.setFillColor(colors.HexColor("#1e293b"))  # Slate 800
        self.drawString(54, 750, "RegIQ Compliance Artifact — Chat Thread Audit")
        
        # Border Separation Rule Line
        self.setStrokeColor(colors.HexColor("#cbd5e1"))  # Slate 300
        self.setLineWidth(0.5)
        self.line(54, 742, 558, 742)
        
        # Footer Content Base Layout
        self.setFont("Helvetica-Oblique", 7.5)
        self.setFillColor(colors.HexColor("#64748b"))  # Slate 500
        
        # Explicit Non-Legal Disclaimer requirement
        disclaimer_text = "Disclaimer: This document is an automated summary generated by RegIQ. This is not formal legal advice."
        self.drawString(54, 42, disclaimer_text)
        
        # Page Tracking Coordinates
        page_str = f"Page {self._pageNumber} of {page_count}"
        self.setFont("Helvetica", 8)
        self.drawRightString(558, 42, page_str)
        
        self.restoreState()


