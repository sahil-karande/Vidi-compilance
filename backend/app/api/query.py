@router.get("/export/{thread_id}")
async def export_thread_pdf(
    thread_id: str,
    user: User | None = Depends(get_optional_user)
):
    """
    Assembles historical chronological thread logs and nested structural citations
    from Supabase and returns a fully aligned, structured, legal PDF artifact.
    """
    supabase_admin = get_supabase_admin()
    
    # 1. Fetch thread contextual envelope
    try:
        thread_res = supabase_admin.table("threads").select("*").eq("id", thread_id).execute()
        if not thread_res.data:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Target message thread not found.")
        thread_info = thread_res.data[0]
    except Exception as err:
        logger.error(f"[Export Lookup Failure] Thread details could not be found: {str(err)}")
        raise HTTPException(status_code=500, detail="Database look-up fallback exception.")

    # 2. Extract historical chat chronological message list
    try:
        messages_res = (
            supabase_admin.table("messages")
            .select("*")
            .eq("thread_id", thread_id)
            .order("created_at", ascending=True)
            .execute()
        )
        messages = messages_res.data or []
    except Exception as err:
        logger.error(f"[Export Data Error] Failed to aggregate message records: {str(err)}")
        raise HTTPException(status_code=500, detail="Database messaging ledger look-up failed.")

    if not messages:
        raise HTTPException(status_code=status.HTTP_400_BAD_REQUEST, detail="Empty workspaces cannot be exported.")

    # 3. Formulate standard Flowable In-Memory buffers
    pdf_buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        pdf_buffer,
        pagesize=letter,
        leftMargin=54,
        rightMargin=54,
        topMargin=72,
        bottomMargin=72
    )

    # 4. Initialize Flowable Paragraph Styling Parameters
    styles = getSampleStyleSheet()
    
    title_style = ParagraphStyle(
        'DocTitle',
        parent=styles['Normal'],
        fontName='Helvetica-Bold',
        fontSize=18,
        leading=22,
        textColor=colors.HexColor("#0f172a"),  # Slate 900
        spaceAfter=12
    )
    
    meta_style = ParagraphStyle(
        'DocMeta',
        parent=styles['Normal'],
        fontName='Helvetica',
        fontSize=9,
        leading=12,
        textColor=colors.HexColor("#475569"),  # Slate 600
        spaceAfter=20
    )

    query_style = ParagraphStyle(
        'UserQuery',
        parent=styles['Normal'],
        fontName='Helvetica-Bold',
        fontSize=11,
        leading=15,
        textColor=colors.HexColor("#1e3a8a"),  # Deep Corporate Blue
        spaceBefore=10,
        spaceAfter=6
    )

    answer_style = ParagraphStyle(
        'AssistantAnswer',
        parent=styles['Normal'],
        fontName='Helvetica',
        fontSize=10,
        leading=14,
        textColor=colors.HexColor("#334155"),  # Slate 700
        spaceAfter=8
    )

    cit_title_style = ParagraphStyle(
        'CitTitle',
        parent=styles['Normal'],
        fontName='Helvetica-Bold',
        fontSize=9,
        leading=11,
        textColor=colors.HexColor("#0f172a")
    )

    cit_body_style = ParagraphStyle(
        'CitBody',
        parent=styles['Normal'],
        fontName='Helvetica-Oblique',
        fontSize=8.5,
        leading=12,
        textColor=colors.HexColor("#475569")
    )

    story = []

    # Compile Header Structural Blocks
    story.append(Paragraph(f"Compliance Audit Trail: {thread_info.get('title', 'Regulation Summary')}", title_style))
    
    gen_time = datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S UTC")
    scopes = ", ".join(thread_info.get("corpus_tags", ["GENERAL"])) or "GENERAL"
    story.append(Paragraph(f"<b>Generated At:</b> {gen_time} | <b>Active Scopes:</b> {scopes}", meta_style))
    story.append(Spacer(1, 10))

    # 5. Extract Q&A blocks and pool unique global citations references
    last_query = ""
    global_citations = {}

    for msg in messages:
        role = msg.get("role")
        content = msg.get("content", "").strip()

        if role == "user":
            last_query = content
            
        elif role == "assistant" and last_query:
            qa_block = []
            qa_block.append(Paragraph(f"<b>Query:</b> {last_query}", query_style))
            qa_block.append(Spacer(1, 4))
            
            # Format lines explicitly to allow paragraphs matching textbreaks
            sanitized_answer = content.replace("\n", "<br/>")
            qa_block.append(Paragraph(sanitized_answer, answer_style))
            
            citations_array = msg.get("citations") or []
            local_indexes = []
            
            for cit in citations_array:
                if isinstance(cit, dict):
                    chunk_id = cit.get("chunk_id") or str(uuid.uuid4())
                    if chunk_id not in global_citations:
                        global_citations[chunk_id] = {
                            "index": len(global_citations) + 1,
                            "title": cit.get("title") or "Compliance Notification Reference",
                            "circular_no": cit.get("circular_no") or "N/A",
                            "date": cit.get("date") or "N/A",
                            "section": cit.get("section") or "General Clause",
                            "excerpt": cit.get("excerpt") or cit.get("preview") or ""
                        }
                    local_indexes.append(str(global_citations[chunk_id]["index"]))
            
            if local_indexes:
                citation_line = f"<b>Grounding Citations:</b> Source Artifact [{', '.join(local_indexes)}]"
                qa_block.append(Paragraph(citation_line, ParagraphStyle('CitLine', parent=styles['Normal'], fontName='Helvetica-Bold', fontSize=8.5, textColor=colors.HexColor("#16a34a"))))
            
            qa_block.append(Spacer(1, 12))
            story.append(KeepTogether(qa_block))
            last_query = ""  # Flush loop pair

    # 6. Append Unified Grounded Evidence Appendix Block
    if global_citations:
        story.append(Spacer(1, 10))
        story.append(Paragraph("Verified Evidence Appendix", styles['Heading2']))
        story.append(Spacer(1, 6))
        
        for _, details in sorted(global_citations.items(), key=lambda x: x[1]['index']):
            idx = details["index"]
            ref_header = f"<b>[{idx}] {details['title']}</b> (Circular: {details['circular_no']} | Date: {details['date']} | Section: {details['section']})"
            
            citation_elements = [
                Paragraph(ref_header, cit_title_style),
                Spacer(1, 2)
            ]
            
            if details["excerpt"]:
                clean_excerpt = details["excerpt"].strip().replace("\n", "<br/>")
                citation_elements.append(Paragraph(f"&ldquo;... {clean_excerpt} ...&rdquo;", cit_body_style))
            
            citation_elements.append(Spacer(1, 8))
            story.append(KeepTogether(citation_elements))

    # Construct Document Structure via Canvas Makers
    doc.build(story, canvasmaker=NumberedCanvas)
    pdf_buffer.seek(0)
    
    clean_filename = f"RegIQ_Report_{thread_id[:8]}.pdf"
    return StreamingResponse(
        pdf_buffer, 
        media_type="application/pdf", 
        headers={"Content-Disposition": f"attachment; filename={clean_filename}"}
    )